class RabbitMqConfiguration:
    def __init__(self):
        self.__exchangeName = ''
        self.__host = ''
        self.__port = 0
        self.__virtualHost = ''
        self.__user = ''
        self.__password = ''

    @property
    def exchangeName(self):
        return self.__exchangeName


    @exchangeName.setter
    def exchangeName(self, value):
        self.__exchangeName = value

    @property
    def host(self):
        return self.__host

    @host.setter
    def host(self, value):
        self.__host = value

    @property
    def port(self):
        return self.__port

    @port.setter
    def port(self, value):
        self.__port = value

    @property
    def virtualHost(self):
        return self.__virtualHost

    @virtualHost.setter
    def virtualHost(self, value):
        self.__virtualHost = value

    @property
    def user(self):
        return self.__user

    @user.setter
    def user(self, value):
        self.__user = value

    @property
    def password(self):
        return self.__password

    @password.setter
    def password(self, value):
        self.__password = value
